    [MetaDescription]   NVARCHAR (MAX)   DEFAULT ('') NOT NULL,
    [SortOrder]         INT              DEFAULT ((0)) NOT NULL,
    [ShowDetail]        BIT              DEFAULT ((1)) NOT NULL,
    [PageTitle]         NVARCHAR (1024)  DEFAULT ('') NOT NULL,
    [ContentManagerId]  UNIQUEIDENTIFIER NULL,
    [DocumentManagerId] UNIQUEIDENTIFIER NULL,
    [ERPProductValues]  NVARCHAR (MAX)   DEFAULT ('') NOT NULL,
    [IsFeatured]        BIT              DEFAULT ((0)) NOT NULL,
    [IsDynamic]         BIT              DEFAULT ((0)) NOT NULL,
