    [Name]              NVARCHAR (255)   NOT NULL,
    [ShortDescription]  NVARCHAR (255)   DEFAULT ('') NOT NULL,
    [SmallImagePath]    NVARCHAR (1024)  DEFAULT ('') NOT NULL,
