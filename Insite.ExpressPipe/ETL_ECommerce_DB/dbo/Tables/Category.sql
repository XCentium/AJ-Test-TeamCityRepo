    [ModifiedOn]        DATETIME2 (7)    DEFAULT (getdate()) NOT NULL,
    [ModifiedBy]        NVARCHAR (100)   DEFAULT ('') NOT NULL,
    [Path]              NVARCHAR (1024)  DEFAULT ('') NOT NULL,
    [ParentPath]        NVARCHAR (1024)  DEFAULT ('') NOT NULL
